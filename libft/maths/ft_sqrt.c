float			ft_sqrt(float nb)
{
	float	n;
	int		i;

	if (nb <= 1)
		return (nb == 1 ? 1 : 0);
	n = 1;
	i = 23;
	while (--i)
		n = (0.5) * (n + nb / n);
	return (n);
}
